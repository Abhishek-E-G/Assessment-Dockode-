def pattern(rows, cols):
    for row in range(rows):
        for col in range(cols):
            print(" ___ ", end='')
        print()
        for col in range(cols):
            print("/   \\", end='')
        print()
        for col in range(cols):
            print("\\___/", end='')
        print("")

# First sample
print("inputs :- 4 7")
pattern(4, 7)

# second sample
print("inputs :- 3 5")
pattern(3, 5)

