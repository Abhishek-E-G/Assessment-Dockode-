def pattern(rows, cols):
    for i in range(1,rows+1):
        
        for j in range(1,cols+1):

            if i < 2:
                print(" ___",end='')
                print("  ",end='  ')
            
        print()
        for k in range(1,cols+1):
                
            print("/   \\",end='')
        
            if k < cols:
                print("___",end='')
   
        print()
        for l in range(1,cols+1):
            print("\\___/ ",end='  ')


    print("\n")

# First sample
print("inputs :- 4 7")
pattern(4, 7)

# second sample
print("inputs :- 3 5")
pattern(3, 5)

